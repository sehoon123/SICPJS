function f(n) {
    return A(0, n);
}
// f(n) = 2 * n
function g(n) {
    return A(1, n);
}
// g(n) = 2 ^ n
function h(n) {
    return A(2, n);
}
// h(n) = 2 ^ 2 ^ 2 ^...^2
function k(n) {
    return 5 * n * n;
}
